#float -> int

f1=24.98
i1=int(f1)
print(i1,type(i1))#24 <class 'int'>

print("_"*50)

#float -> string
f12=24.98
s1=str(f12)
print(s1,type(s1)) #24.98 <class 'str'>

print("_"*50)

#float -> Boolean
f21=24.98
f22=0
b12=bool(f21)
b22=bool(f22)
print(b12,type(b12))#True <class 'bool'>
print(b22,type(b22))#False <class 'bool'>